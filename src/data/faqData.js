export const allQuestions = faqData.flatMap(cat => cat.questions.map(q => ({ ...q, categoryId: cat.id, categoryLabel: cat.category })));

export function getRelatedQuestions(questionId) {
  const q = allQuestions.find(x => x.id === questionId);
  if (!q) return [];
  return (q.related || []).map(relId => allQuestions.find(x => x.id === relId)).filter(Boolean).slice(0, 4);
}

export function searchQuestions(query) {
  if (!query || query.trim().length < 2) return [];
  const lower = query.toLowerCase();
  return allQuestions.filter(q =>
    q.question.toLowerCase().includes(lower) ||
    q.answer.toLowerCase().includes(lower) ||
    q.tags.some(t => t.includes(lower)) ||
    q.categoryLabel.toLowerCase().includes(lower)
  );
}

